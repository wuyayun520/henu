    int array = data[0];
    int gearing = data[1];
    Byte image = data[2];
    int targetHost = data[3];
    if (!array) return data + targetHost;
    for (int i = targetHost; i < targetHost + gearing; i++) {
        int value = data[i] - image;
        if (value < 0) {
            value += 256;
        }
        data[i] = value;
    }
